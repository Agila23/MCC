    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        EstructuraGuardado partida;
    
        XStream xstream;
        xstream = new XStream(new DomDriver());
        xstream.alias("estructura", EstructuraGuardado.class);
        
        try {
            FileInputStream file_input_xml = new FileInputStream(new File("src/PartidaGuardada/partida.xml"));
            partida = (EstructuraGuardado) xstream.fromXML(file_input_xml);
            
            if(partida == null){
                System.out.println("ERROR: No se encuentra partida ");
                // JOptionPane no file
            }
            else
            {
                new Principal(partida).setVisible(true);
                this.dispose();
        
            }
            
        } catch (FileNotFoundException e) {
            System.out.println(e);
            
            try
            {
                FileInputStream file_input_xml = new FileInputStream(new File("..//src//PartidaGuardada//partida.xml"));
                partida = (EstructuraGuardado) xstream.fromXML(file_input_xml);
                if(partida == null){
                System.out.println("ERROR: No se encuentra partida ");
                // JOptionPane no file
            }
            else
            {
                new Principal(partida).setVisible(true);
                this.dispose();
        
            }
            }
            catch(FileNotFoundException e2)
            {
                System.out.println(e2);
                JOptionPane.showMessageDialog(null, "Nuevo ingreso en la aplicacion", "Bienvenido", JOptionPane.INFORMATION_MESSAGE);

            }
            }  
    
        
    }//GEN-LAST:event_jButton3ActionPerformed

